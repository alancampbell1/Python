"""
Python Variables
Python has no command for declaring a variable. A variable is created when you first 
assign it a value.
"""

x = 5
y = "Alan"
print(x)
print(y)


"""
Variables do not need to be declared with a particular type and can even change type after
it has been set.
"""

x = 4			#x has a type of int
x = "Niall"		#x has a type of str
print(x)


"""
Rules for Python Variables:
- Must start with a letter or an underscore
- Cannot start with a number
- Can only contain alpha-numeric characters and underscores
- Names can be case sensitive
"""

"""
You can combine both text and a variable using the + symbol
"""

x = "awesome"
print("Python is " + x)

x = "Python is "
y = "awesone"
z = x + y
print(z)

"""
The + can also be used to perform Mathematical calculations
"""

x = 5
y = 10
print(x + y)

