"""
Python Numbers

There are three numeric types in Python: int, float & complex
"""

x = 1		#int
y = 2.8		#float
z = 1j		#complex

#To find out their type use the type() function

print(type(x))
print(type(y))
print(type(z))


"""
Int, or Integer, is a whole number, positive or negative, without decimals, of unlimitied
length
"""

x = 1;
y = 12348888838483202947
z = -5643823

print(type(x))
print(type(y))
print(type(z))

"""
Float or Floating Point Number is a number, positive or negative, containing one or more
decimals
"""

x = 1.10 
y = 1.0
z = -35.58

#They can also be scientific numbers with an "e" to indicate the power of 10
x = 35e3
y = 12E4
z = -87.7e100



"""
Complex numbers are written with a 'j' as the imaginary part
"""

x = 3 +5j
y = 5j
z = -5j






